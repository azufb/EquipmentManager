<template>
  <v-app id='App'>
    <v-col :key='12'
      cols=12 sm=10 md=8 lg=4 xl=3>
    <v-app-bar
    app 
    color='#B2EBF2'
    >
      <div>
        <h1>Equipment Manager</h1>
      </div>
    </v-app-bar>

    <v-main>
      <v-form v-on:submit.prevent='onclick'>
        <label for='name'>品目名：</label>
        <v-text-field label='品目名を入力してください。' 
          single-line solo
          id='name' v-model='name' required='required'></v-text-field>
        <v-btn class='subbtn' type='submit'>登録</v-btn>
      </v-form>
      <ul v-for='(item, index) in items' :key='index'>
        <li>
          {{ item.name }}
          <div>
            <v-btn class='btn' color='#80DEEA' v-on:click='deleteItem(item.name)'>DELETE</v-btn>
            <v-btn class='btn' color='#80DEEA' v-on:click='plus(item.name)'>PLUS</v-btn>
            {{ item.count }}
            <v-btn class='btn' color='#80DEEA' v-on:click='minus(item.name)'>MINUS</v-btn>
          </div>
        </li>
      </ul>
    </v-main>
    </v-col>
  </v-app>
</template>

<script>
export default {
  name: 'App',
  computed: {
    items() {
      return this.$store.getters.items
    },
    count() {
      return this.$store.getters.items.count
    }
  },
  data() {
    return {
      name: ''
    }
  },
  methods: {
    onclick() {
      this.$store.commit('addItem', {
        item: {
          name: this.name,
          count: 0
        }
      })
      this.name = ''
    },
    deleteItem(name) {
      this.$store.commit('deleteItem', name)
    },
    plus(name) {
      this.$store.commit('plus', name)
    },
    minus(name) {
      this.$store.commit('minus', name)
    }
  }
};
</script>

